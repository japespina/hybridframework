    private void userLogin(String givenLoginId){
        JFrame jFrame = new JFrame();
        
        if(givenLoginId.equals("")){
            JOptionPane.showMessageDialog(jFrame, "Login ID has invalid value or empty", "Login Page", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                Connection conn = ewb.qa.tdd.SQLObj.ConnToDB();
                CallableStatement cs = conn.prepareCall("{call Search_UserProfile_ByUserId(?)}");
                cs.setNString(1, givenLoginId);
                
                ResultSet rs = cs.executeQuery();
                if(rs.next()){
                    /*		
                    UP.ID,
                    UP.USERID,
                    UP.USERTYPE
                    */
                    int UPId = rs.getInt(1);
                    String UPUserId = rs.getNString(2);
                    int UPUserType = rs.getInt(3);

                    setLoginId(UPUserId.toUpperCase());                                    
                    setUserType(UPUserType);
                    
                    setVisible(false);
                    MainGUI form = new MainGUI();
                    form.setVisible(true);            
                }
                else{
                    JOptionPane.showMessageDialog(jFrame, "User Login ID is not yet registered", "TDD Login Page", JOptionPane.INFORMATION_MESSAGE);
                }
                conn.close();

            }
            catch(Exception ex){
                    String errMessage = null;

                    errMessage = "Error Message: " + ex.getMessage() + 
                       "\n" + "Error Localize Message: " + ex.getLocalizedMessage() + 
                       "\n" + "Stack Trace: " + ex.getStackTrace() + 
                       "\n" + "Cause: " + ex.getCause();
                    
                JOptionPane.showMessageDialog(jFrame, "Database connection failed, contact your Administator for assistance." + "\n" + errMessage
                        , "Login Page", JOptionPane.ERROR_MESSAGE);
            }
        }        
    }
            
