    private boolean validate_userprofile(String givenUserId){
        JFrame jFrame = new JFrame();
        boolean result = false;
        
        try{
            Connection conn = ewb.qa.tdd.SQLObj.ConnToDB();
            CallableStatement cs = conn.prepareCall("{call Search_UserProfile_ByUserId(?)}");
            cs.setNString(1, givenUserId);
            
            ResultSet rs = cs.executeQuery();
            if(rs.next()){
                /*
                1-UP.ID,
                2-UP.USERID,
                3-UP.USERTYPE                
                */
                
                setUserType(rs.getInt(3));
                result = true;
            }
            else{
                result = false;
            }
            
            rs.close();
            cs.close();
            conn.close();
            
            return result;
        }
        catch(Exception ex){
                    String errMessage = null;

                    errMessage = "Error Message: " + ex.getMessage() + 
                       "\n" + "Error Localize Message: " + ex.getLocalizedMessage() + 
                       "\n" + "Stack Trace: " + ex.getStackTrace() + 
                       "\n" + "Cause: " + ex.getCause();
                    
                JOptionPane.showMessageDialog(jFrame, "User validation failed." + "\n" + errMessage
                        , "Login Page", JOptionPane.ERROR_MESSAGE);            
            return result;
        }
    }
    
